public class OperadoresLogico {


    public static void main(String[] args) {
        
        int valor1 = 1;
        int valor2 = 2;

        boolean resultado1 = (valor1 == valor1) && (valor2 == valor2);
        System.out.println("Valor1  é 1 AND Valor2 é 2  - resultudado " + resultado1);

        boolean resultado2 = (valor1 == valor1) || (valor2 == valor2);
        System.out.println("Valor 1 é 1 OR Valor2 é 2 - resultado  " + resultado2);






    }
    
}
